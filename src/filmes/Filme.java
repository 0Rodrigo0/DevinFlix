package filmes;

// Sempre com letra maiuscula
public class Filme {

    public String nome;
    public String genero;
    public String sinopse;
    public String link;

// forma de criar filme
    Filme(String nome, String genero, String sinopse, String link) {
        this.nome = nome;
        this.genero = genero;
        this.sinopse = sinopse;
        this.link = link;

    }

}
